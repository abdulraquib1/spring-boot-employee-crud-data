package com.raq.springboot.dto;

import com.fasterxml.jackson.annotation.JsonIgnore;

public class EmployeeV2 {

	private int id;
	private String name;
	
	@JsonIgnore
	private int age;
	
	@JsonIgnore
	private double salary;
	
	
	public EmployeeV2(int id, String name, int age, double salary) {
		super();
		this.id = id;
		this.name = name;
		this.age = age;
		this.salary = salary;
	}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public double getSalary() {
		return salary;
	}
	public void setSalary(double salary) {
		this.salary = salary;
	}
	
}
